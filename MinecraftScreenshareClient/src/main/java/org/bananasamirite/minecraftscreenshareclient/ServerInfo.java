package org.bananasamirite.minecraftscreenshareclient;

public class ServerInfo {
    private int sizeX;
    private int sizeY;

    public ServerInfo() {}

    public ServerInfo(int sizeX, int sizeY) {
        this.sizeX = sizeX;
        this.sizeY = sizeY;
    }

    public int getSizeX() {
        return sizeX;
    }

    public void setSizeX(int sizeX) {
        this.sizeX = sizeX;
    }

    public int getSizeY() {
        return sizeY;
    }

    public void setSizeY(int sizeY) {
        this.sizeY = sizeY;
    }
}
